import numpy as np


def sin(theta):
    return np.sin(theta)


def cos(theta):
    return np.cos(theta)


def tan(theta):
    return np.tan(theta)


def asin(theta):
    return np.arcsin(theta)


def acos(theta):
    return np.arccos(theta)


def atan(theta):
    return np.arctan(theta)

